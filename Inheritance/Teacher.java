package inheritance_package;         //single level inheritance

public class Teacher {       //super class/ parent class
	static void selenium()   //static method of parent class
	{
		System.out.println("Manish will teach selenium");
	}
	void sql()               //non static method of parent class
	{
		System.out.println("Manish will teach sql");
	}
	static void java()
	{
		System.out.println("Manish will teach java");
	}

}
