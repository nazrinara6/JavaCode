package inheritance_package;

public class Math_Class_Program {

	public static void main(String[] args) {
	    
		
	    System.out.println(Math.addExact(3, 4));
	    System.out.println(Math.subtractExact(10, 4));
	    System.out.println(Math.multiplyExact(13, 3));
	    System.out.println(Math.min(2, 6));
	    System.out.println(Math.max(40, 20));
	    System.out.println(Math.abs(-300));
	    System.out.println(Math.random());

	}

}
