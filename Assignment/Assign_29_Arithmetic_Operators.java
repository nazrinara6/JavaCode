package core_java_assignment;

public class Assign_29_Arithmetic_Operators 
// Arithmtic Operators
{
	    static int a = 100; 
		static int b = 200; 
		
		static void add() 
		{
			System.out.println(a+b);
		}
		static void sub() 
		{
			System.out.println(b-a);
		}
		static void mul() 
		{
			System.out.println(a*b);
		}
		static void div() 
		{
			System.out.println(b/a);
		}
		static void mod() 
		{
			System.out.println(b%a);
		}

		public static void main(String[] args) {
			add();
			sub() ;
			mul();
			div();
			mod();
		}

	}
